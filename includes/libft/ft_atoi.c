#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int	i;
	int	valeur;
	int	sign;

	i = 0;
	valeur = 0;
	sign = 1;
	while (nptr[i] == ' ' || (nptr[i] >= 9 && nptr[i] <= 13))
		i++;
	if (nptr[i] == '-')
	{
		sign = -1;
		i++;
	}
	else if (nptr[i] == '+')
		i++;
	while (nptr[i] != '\0' && (nptr[i] >= '0' && nptr[i] <= '9'))
	{
		valeur = valeur * 10 + nptr[i] - '0';
		i++;
	}
	return (sign * valeur);
}
